public class Poxody2 {
    private String otrad;
    private String gora;
    private String datavos;

    public Poxody2(String otrad, String gora, String datavos) {
        this.otrad = otrad;
        this.gora = gora;
        this.datavos = datavos;
    }

    public String getOtrad() {
        return otrad;
    }

    public void setOtrad(String otrad) {
        this.otrad = otrad;
    }

    public String getGora() {
        return gora;
    }

    public void setGora(String gora) {
        this.gora = gora;
    }

    public String getDatavos() {
        return datavos;
    }

    public void setDatavos(String datavos) {
        this.datavos = datavos;
    }
}
